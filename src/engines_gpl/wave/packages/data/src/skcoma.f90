    implicit none
!
! Global variables
!
    integer, intent(in)  :: iuni
!
! Local variables
!
    character(1) :: ch
!
!! executable statements -------------------------------------------------------
!
   10 continue
    read (iuni, '(A)') ch
    if (ch=='*') goto 10
    backspace (iuni)
